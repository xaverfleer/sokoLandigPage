  function getFormData() {
    const entries = [].slice.call(formElem.elements);

    const data = entries.reduce(
      (acc, elem) => (elem.value ? { ...acc, [elem.name]: elem.value } : acc),
      {}
    );
    return JSON.stringify(data);
  }

