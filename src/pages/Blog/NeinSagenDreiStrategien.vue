      <p class="blog__date-and-author">07. März 2021 – Eva Fleer</p>
      <h1 class="blog__title">
        «Nein» sagen, aber wie? <br />Drei Strategien, die alle Eltern kennen
        sollten!
      </h1>
      <p>
        «Nein, ich habe jetzt keine Zeit, um mit dir ein Buch anzuschauen» /
        «Nein, bitte nicht auf den Tisch klettern» / «Nein, du darfst nicht mit
        dem Messer spielen, das ist zu gefährlich» / «Nein, wir gehen jetzt
        nicht nochmals nach draussen» / «Nein, wir schauen nicht noch eine
        weitere Folge» / «Nein, du kannst nicht auf dem Beifahrersitz mitfahren,
        du musst in deinem Kindersitz bleiben»…
      </p>
      <p>
        Manchmal ist Elternsein echt anstrengend. Immer wieder tun unsere Kinder
        etwas, das wir nicht zulassen können oder sie haben Wünsche, die wir
        nicht erfüllen wollen. Immer wieder hören wir uns «Nein» sagen. Ohne
        «Nein» geht es nicht. Kinder haben das Recht, zu erfahren, wenn sie sich
        in Gefahr bringen, wenn sie Grenzen anderer Menschen überschreiben oder
        wenn sie Grundwerte verletzen. Sie haben aber auch das Recht, dies auf
        eine respektvolle Weise zu erfahren, ohne in ihrer Würde verletzt zu
        werden. Wie das gehen kann, zeigt zum Beispiel Thomas Gordon mit dem
        Konzept der <em>Ich-Botschaft</em> im Buch
        <em>Familienkonferenz</em> auf:
      </p>
      <ol>
        <li>
          Benenne das Verhalten, das du nicht akzeptierst oder den Wunsch, den
          du nicht erfüllen kannst: <br />«Du möchtest gerne noch länger auf dem
          Spielplatz bleiben.»
        </li>
        <li>
          Benenne welche Gefühle ein Verhalten oder ein Wunsch in dir auslöst:
          <br />«Ich werde langsam nervös und ungeduldig.»
        </li>
        <li>
          Benenne eine konkrete und greifbare negative Wirkung/Folge:
          <br />«Wenn wir jetzt nicht bald los gehen, habe ich nachher Stress
          beim Kochen.»
        </li>
      </ol>
      <p>
        Das ist eine hilfreiche Methode. Sie hilft mir, herablassende,
        beschuldigende Aussagen zu vermeiden und mein «Nein» klar zu begründen.
        Doch das «Nein-Sagen» wird dadurch nicht weniger anstrengend. In diesem
        Beitrag zeige ich drei ergänzende Strategien auf, die mir als Mutter das
        «Nein-Sagen» deutlich erleichtern. Erleichtern deswegen, weil sie mir
        ermöglichen zugewandt zu bleiben und mir nicht das Gefühl geben, meine
        Tochter mit dem Nein von mir wegzustossen.
      </p>
      <br />
      <h2>1. Strategie: Ein «Ja» unter Bedingungen</h2>
      <g-image
        alt="Baby playing with bottle"
        class="img"
        src="~/assets/imgs/Baby-spielt-mit-Flasche.png"
      />
      <p>
        Wenn ein Kind einen Wunsch äussert, den wir nicht erfüllen wollen, oder
        etwas tut, das wir nicht zulassen können, sagen wir oftmals erstmals
        kategorisch «Nein»: «Nein, ich habe jetzt keine Zeit, mit dir ein
        Bilderbuch zu schauen», «Nein, ich mag dich nicht noch ein zweites Mal
        baden», «Nein, da kannst du dich nicht hochziehen, das Regal könnte
        umkippen».
      </p>
      <p>
        Oft übersehen wir, dass ein Wunsch durchaus erfüllbar ist oder ein
        Verhalten tolerierbar, einfach unter anderen Bedingungen als es das Kind
        gerade möchte: Zum Beispiel zu einem anderen Zeitpunkt, mit einer
        anderen Person, mit einem anderen Gegenstand, an einem anderen Ort usw.
      </p>
      <p>
        Wenn ich z.B. gerade koche und meine Tochter will mit mir ein Bilderbuch
        schauen, dann ist ihr Wunsch jetzt gerade nicht erfüllbar, aber nach dem
        Essen möglicherweise schon. Oder vielleicht ist Papa verfügbar, um ihren
        Wunsch zu erfüllen. Anstatt also schnell durch ein «Nein» potenziell
        Widerstand und Frust zu erzeugen, kann ich ihren Wunsch bejahen und
        hinzufügen, unter welchen Bedingungen ich ihn erfüllen kann: «Ja, wir
        können ein Bilderbuch schauen. Jetzt koche ich noch, aber nach dem Essen
        habe ich Zeit» oder «Ja, du kannst dieses Bilderbuch schauen, ich bin
        gerade beschäftigt, aber Papa hat Zeit für dich. Magst du ihn fragen?»
      </p>
      <p>
        Anmerkung: Wenn ich die Erfüllung eines Wunsches auf einen späteren
        Zeitpunkt verschiebe, dann stelle ich mir oft einen Wecker auf dem
        Handy, damit ich es nicht vergesse. Der Wecker erinnert mich dann z.B.
        nach dem Essen daran, meine Tochter zu fragen, ob sie immer noch ein
        Buch gemeinsam schauen will.
      </p>
      <p>
        Andere Beispiele:
      </p>
      <ul>
        <li>
          «Ja, du darfst dich zum Stehen hochziehen, aber bei diesem Regal ist
          es zu gefährlich. Du kannst das Sofa nutzen.»
        </li>
        <li>
          «Ja, du darfst mit deiner Trommel spielen, nur mir ist es gerade zu
          laut. Du kannst ins Wohnzimmer gehen oder ein Kissen auf die Trommel
          legen».
        </li>
        <li>
          «Ja, du kannst noch einen Nachtisch haben. Dass du (noch mehr)
          Schokolade isst, kann ich aber gerade nicht verantworten. Wie wär’s
          mit einem Fruchtsalat?»
        </li>
        <li>
          «Ja, wir werden deine Patentante wieder einmal besuchen, ich muss aber
          erst noch ein Datum mit ihr vereinbaren.»
        </li>
        <li>
          «Ja, du kannst mit diesem Schaukelpferd spielen, sobald das andere
          Kind nicht mehr will und es frei ist.»
        </li>
      </ul>
      <p>
        Indem wir auf diese Weise Wünsche, Handlungen und Interessen mit
        gewissen Einschränkungen bzw. unter gewissen Bedingungen bejahen, fühlt
        sich unser Kind viel eher gesehen und verstanden, als wenn wir einfach
        kategorisch «Nein» sagen.
      </p>
      <br />
      <h2>
        2. Strategie: Dem Wunsch in der Fantasie nachgehen <br />oder ein
        Verhalten in der Fantasie ermöglichen
      </h2>
      <g-image
        alt="Baby and rabbit"
        class="img"
        src="~/assets/imgs/Kind-und-Hase.png"
      />
      <p>
        Es gab bei uns vor kurzem eine Phase, in der meine Tochter immer kurz
        vor dem Einschlafen nochmals aufstehen und nach unten ins Wohnzimmer
        oder sogar nach draussen gehen wollte. Das konnte ich nicht ermöglichen,
        ich war einfach zu müde. Ich sagte dann zuerst klar, aber möglichst
        liebevoll: «Nein, wir stehen jetzt nicht noch einmal auf, ich bin
        wirklich zu müde dazu!». Das hat meine Tochter natürlich nicht einfach
        so akzeptiert: «Ich will aber…». Das wäre dann die ideale Vorlage
        gewesen, um in ein endloses Hin-und-Her zwischen «Ich will aber…» und
        «Das geht nicht, ich habe dir doch gesagt…» zu geraten. Vermutlich wäre
        ich ärgerlich geworden und meine Tochter hätte irgendwann geweint.
      </p>
      <p>
        Zum Glück hatte ich kurz vor dieser
        <em>Ich-Will-Nochmal-Aufstehen-Phase</em> das Buch
        <em>So sag ich’s meinem Kind</em> von Adele Faber und Elaine Mazlish
        gelesen und die folgende Strategie kennengelernt: Gib den Wünschen
        deines Kindes in der Fantasie nach!
      </p>
      <p>
        Anstatt mein Argument, dass ich zu müde sei, immer und immer wieder
        vorzubringen und doch damit nicht auf mehr Akzeptanz zu stossen, habe
        ich mir angewöhnt, meine Tochter Folgendes zu fragen: «Wollen wir uns
        vorstellen, wie es wäre, wenn wir jetzt nochmals aufstehen würden?».
        Darauf ist sie eingegangen und wir haben in der Fantasie noch die
        wildesten Dinge unternommen. Wir sind z.B. noch in der Aare schwimmen
        gegangen oder haben Pfannkuchen gekocht. Wir haben uns vorgestellt, wie
        wir den Mond und die Sterne sehen und vielleicht einer Katze begegnen,
        die wir streicheln könnten. Vielleicht war die Katze grün oder blau oder
        hatte sieben Ohren. Während unseren Fantasiereisen ist meine Tochter oft
        eingeschlafen.
      </p>
      <p>
        <strong
          >Wenn wir nicht erfüllbaren Wünschen in der Fantasie nachgehen oder
          wenn wir Verhalten in der Fantasie ermöglichen, das in der Realität
          nicht möglich ist, dann kann das sehr viel Entspannung in den
          Familienalltag bringen.</strong
        >
      </p>
      <p>
        Wie viel Entspannung dieses Instrument bringen kann, zeigt auch das
        folgende Beispiel:
      </p>
      <p>
        In den Skiferien begegneten wir auf dem Weg ins Dorf einem Vater mit
        seiner Tochter. Wir kannten uns nicht, kamen aber ins Gespräch. Ungefähr
        zehn Minuten, bevor wir im Dorf waren, sagte das Mädchen, es habe Durst
        und wolle etwas trinken. Niemand von uns hatte etwas zu trinken dabei.
        Der Vater versuchte logisch zu argumentieren: «Wir haben jetzt nichts zu
        trinken, aber wir sind in zehn Minuten im Dorf, dann kann ich dir eine
        Flasche Wasser kaufen». Das Mädchen wurde weinerlich und beharrte auf
        seinem Wunsch, jetzt etwas zu trinken. Der Vater wurde immer
        verzweifelter und ärgerlicher, weil das Mädchen auf seine Argumente
        nicht einging. Und dann hörte ich meinen Mann zum Mädchen sagen: «Du
        hast wohl wirklich grossen Durst. Wenn ich jetzt eine Flasche Wasser
        hinzaubern könnte, wie gross wäre sie? So?» Er machte eine Geste mit
        seiner Hand. Augenblicklich fing das Mädchen an zu schmunzeln und
        meinte: «Nein, so!» Und machte ein viel grössere Geste. Und er
        antwortete: «So gross? Echt? Das wäre wirklich viel Wasser. Ach, wie
        schön, wenn wir zaubern könnten». Damit war die Sache erledigt, es
        kehrte wieder Entspannung ein und die restlichen zehn Minuten
        fantasierten wir alle darüber, was wir alles tun würden, wenn wir
        zaubern könnten.
      </p>
      <br />
      <h2>
        3. Strategie: Nein zum Wunsch oder zum Verhalten, <br />Ja zu Frust,
        Ärger und Trauer
      </h2>
      <g-image
        alt="Baby and father"
        class="img"
        src="~/assets/imgs/Kind-und-Vater.png"
      />
      <p>
        Die folgende Einsicht ist wahnsinnig banal und auch wahnsinnig wichtig:
        Wenn ein Kind an eine Grenze stösst und ein «Nein» hört, dann hat es das
        Recht darauf, frustriert, wütend oder traurig zu sein! Und wir haben
        trotz dieser schwierigen Gefühle das Recht darauf, bei unserem «Nein» zu
        bleiben (denn wir haben ja sicher gute Gründe dafür). Gefühle wie Frust,
        Wut und Trauer kommen in einem normalen und gesunden Nein-Prozess immer
        wieder vor. Wenn wir uns für ein «Nein» entscheiden, müssen und dürfen
        wir also mit ihnen rechnen. Wir haben deswegen nichts falsch gemacht und
        es ist deswegen auch nichts falsch mit unserem Kind. Wir brauchen wegen
        den schwierigen Gefühlen nicht nervös zu werden, sie gehören dazu.
      </p>
      <p>
        <strong
          >Wenn ein Kind frustriert oder verärgert auf ein «Nein» reagiert, dann
          ist das die perfekte Gelegenheit, um vom «Nein-Modus» gleich wieder in
          den «Ja-Modus» zu schalten.</strong
        >
      </p>
      <p>
        Das heisst nicht, dass wir unser «Nein» rückgängig machen. Es heisst,
        dass wir «Ja» zu den Gefühlen sagen, die unser «Nein» beim Kind auslöst.
      </p>
      <p>
        Meine Tochter reagiert oft ganz besonders frustriert und wütend, wenn
        sie gerne auf meinem Handy ein Foto schauen würde und ich ihr das nicht
        erlaube. Oder wenn sie doch mal ein Foto oder ein Video auf dem Handy
        schauen durfte und ich sie dann bitte, das Handy abzuschalten und mir
        zurück zu geben. Jetzt könnte ich Dinge sagen wie «Mach doch nicht so
        ein Theater» oder ich könnte versuchen, sie möglichst schnell von ihrem
        Frust abzulenken. Nicht selten erlebe ich, dass Kinder in solchen
        Frustsituationen ganz schnell einen Schnuller in den Mund gesteckt
        bekommen.
      </p>
      <p>
        <strong
          >Unsere Kinder fühlen sich aber wesentlich besser verstanden und mehr
          geliebt, wenn wir ihnen ihre Enttäuschung über unser «Nein»
          zugestehen, wenn wir Verständnis dafür zeigen und Trost anbieten (z.B.
          in Form von Körperkontakt).</strong
        >
      </p>
      <p>
        Das kann sich dann zum Beispiel folgendermassen anhören: «Du würdest
        wirklich sehr gerne noch ein Video schauen. Ich kann verstehen, dass du
        frustriert bist, dass ich dir das jetzt nicht erlaube.» Manchmal erzähle
        ich auch von eigenen Erinnerungen: «Ich kann mich noch gut erinnern, wie
        frustriert ich war, wenn meine Eltern mir nicht erlaubt haben,
        fernzusehen. Es ist wirklich manchmal nicht einfach auszuhalten, wenn
        man etwas so sehr will und es nicht bekommen kann.» Grundsätzlich gebe
        ich meiner Tochter zu verstehen, dass ihr Frust berechtigt ist. Und ich
        gebe ihr die nötige Zeit, Zuwendung und Nähe, um diesen Frust vorbei
        ziehen zu lassen. Nach solchen Gefühlsgewittern kehrt oft schnell wieder
        Ruhe ein, das Handy ist vergessen und die Neugier führt meine Tochter in
        die nächsten Welterforschungstour.
      </p>
