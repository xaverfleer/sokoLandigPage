      <p>
        <strong>
          Bedürfnisorientierte Erziehung als Wundermittel?
        </strong>
      </p>
      <p>
        Aktuell verbreitet sich in unserer Gesellschaft immer mehr eine
        Erziehungsphilosophie, die sich klar von diesen herkömmlichen
        Anreiz-Methoden abwendet. Die neue Philosophie, auf der auch die
        subjektorientierte Kommunikation aufbaut, nennt sich z.B.
        bedürfnisorientiert, bindungsorientiert oder beziehungsorientiert. Die
        Grundlage für diesen neuen Ansatz sind die wissenschaftlichen
        Erkenntnisse der modernen Bindungs- und Entwicklungsforschung. Diese
        Forschung zeigt, dass sich Kinder kognitiv, psychisch und sozial dann am
        besten entwickeln können, wenn ihre Bedürfnisse gut befriedigt sind,
        wenn sie positive Bindungserfahrungen machen und tragfähige Beziehungen
        aufbauen können. Es zeigt sich, dass mit diesen neuen Ansätzen
        tatsächlich eine sehr gute Ausganslage dafür geschaffen wird, dass
        unsere Kinder mit Begeisterung und Erfolg lernen, dass sie grundsätzlich
        eine glückliche Kindheit erleben und dass sie zu rücksichtsvollen,
        sozialen und kooperativen Menschen heranwachsen.
      </p>
      <p>
        Aufgrund dieser Erkenntnisse lassen sich nicht wenige
        bedürfnisorientierte Erziehungsratgeber direkt oder versteckt zu einem
        alten, letztlich behavioristischen, Versprechen hinreissen: Nutze diese
        oder jene bedürfnisorientierte Strategie und dein Kind wird sich wie
        gewünscht sozial verhalten. Hinzu kommt neu oft noch das Versprechen:
        Das Kind wird dabei glücklich sein.
      </p>
      <br />
      <p>
        <strong
          >Bedürfnisorientierte Strategien, behavioristische
          Versprechen...</strong
        >
      </p>
      <p>
        Ein Beispiel für ein solches Versprechen habe ich gerade kürzlich im
        Buch
        <em
          >«Hand in Hand. Fünf einfache Strategien durch die Höhen und Tiefen
          des Elternseins»</em
        >
        von Patty Wipfler und Schore Tosha gefunden. Die fünf Strategien
        (Wunschzeit, Bleib-Ganz-Ohr, Grenzen-Setzen, Ganz-Ohr-Spiel und
        Gegenseitiges einfühlsames Zuhören) passen wunderbar zur
        bedürfnisorientierten Erziehung und zur subjektorientierten
        Kommunikation. Grundsätzlich kann ich das Buch also durchaus
        weiterempfehlen.
      </p>
      <p>
        Was mir aber auffällt, sind die vielen Beispiele von Eltern, die
        berichten, wie erfolgreich sie die eine oder andere Strategie angewandt
        haben, um das Verhalten ihrer Kinder positiv zu beeinflussen. Mit vielen
        solchen Berichten wird veranschaulicht, was die fünf
        Hand-in-Hand-Strategien bewirken können. In einem Beispiel berichtet
        z.B. eine Mutter wie sie die Strategie «Wunschzeit» erfolgreich
        eingesetzt hat, um den morgendlichen Stress zu reduzieren:
      </p>
      <p>
        <em>
          «Es klappte tatsächlich! Sie putzten sich ohne Druck die Zähne und
          machten sogar selbständig ihr Bett. Ganz erstaunlich.»
        </em>
      </p>
      <p>
        Und da haben wir sie wieder: Die behavioristische Idee, dass eine
        Erziehungsstrategie dann erfolgreich ist, wenn sie das kindliche
        Verhalten positive beeinflusst. Hätte die Wunschzeit bei dieser Mutter
        nicht den entsprechenden Effekt auf das Verhalten der Kinder gehabt,
        hätte sie nach dieser Idee zum Schluss kommen müssen, dass sie nichts
        bringt bzw. dass sie nicht funktioniert. Ob die Wunschzeit z.B. das
        Selbstvertrauen und den Selbstwert der Kinder oder die
        Eltern-Kind-Beziehung stärkt, spielt gemäss dieser Idee keine Rolle, so
        lange die gewünschte Verhaltensänderung ausbleibt.
      </p>
      <br />
      <h2>
        Eltern unter Druck...
      </h2>
      <p>
        Wenn Eltern der Idee verfallen, mit den richtigen Strategien das
        richtige (messbare) Ergebnis erzielen zu können, setzt sie das enorm
        unter Druck. Dabei ist dieser Druck für viele Eltern mit der
        bedürfnisorientierten Erziehung noch grösser geworden, weil die Ratgeber
        als «richtiges» Ergebnis nicht nur das gute, soziale Verhalten, sondern
        auch das glückliche und erfolgreiche Kind versprechen.
      </p>
      <p>
        Jedes Mal, wenn ihr Kind sich dann zum Beispiel asozial verhält,
        Lernschwierigkeiten hat oder unglücklich ist, müssen Eltern annehmen,
        die falschen Strategien gewählt zu haben oder die richtigen Strategien
        falsch eingesetzt zu haben. Und das ist natürlich wahnsinnig
        frustrierend. Der folgende Satz einer Mutter aus einem Elternforum steht
        für mich exemplarisch für diesen Frust:
      </p>
      <p>
        <em>
          «Jedenfalls, wie man es macht, macht man es falsch - irgendwer ist
          gefühlt immer unzufrieden.»
        </em>
      </p>
      <p>
        Bedeutet die momentane Unzufriedenheit eines Kindes wirklich, dass die
        Eltern etwas falsch gemacht haben? Wenn wir die Idee loslasssen, dass
        wir mit der richtigen Erziehung das richtige Ergebnis erzielen können,
        dann können wir diese Frage nur verneinen. Und damit hoffentlich etwas
        Druck wegnehmen.
      </p>
      <br />
      <h2>Am Ende bleibt nur das Vertrauen</h2>
      <p>
        Ich habe grosses Vertrauen in die Ergebnisse der modernen Bindungs- und
        Entwicklungsforschung. Ich vertraue darauf, dass ich meiner Tochter gute
        Voraussetzungen für eine gesunde kognitive, psychische und soziale
        Entwicklung schaffe, indem ich auf ihre Bedürfnisse eingehe und indem
        ich viel darin investiere eine tragfähige und vertrauensvolle Bindung zu
        ihr aufzubauen und aufrechtzuerhalten. Ich vertraue darauf, dass ich
        durch die subjektorientierte Kommunikation ihre innere Stärke Tag für
        Tag nähren kann und ich vertraue darauf, dass diese innere Stärke ihr im
        Leben immer wieder weiterhelfen wird.
      </p>
      <p>
        Gleichzeitig weiss ich, dass keine Erziehungs- und keine
        Kommunikationsstrategie der Welt meiner Tochter das Leben garantiert,
        dass ich mir für sie wünsche. Aus den Erkenntnissen der Wissenschaft
        lässt sich nicht schliessen, dass Kinder, die bedürfnisorientiert
        aufwachsen und weitgehend eine gewaltfreie, subjektorientierte
        Kommunikation erleben, nie Lernschwierigkeiten haben, nie in einer Krise
        stecken und unglücklich sind und sich immer vorbildlich sozial verhalten
        können.
      </p>
      <p>
        <strong
          >Weder der momentane Lernerfolg, noch die momentane Befindlichkeit
          noch das momentane Verhalten eines Kindes sind aus einer
          subjektorientierten Sicht ein guter Massstab für «gelungene»
          Erziehung. Der Massstab ist die innere Stärke, aber die lässt sich
          nicht messen. Daher bleibt am Ende tatsächlich nur das Vertrauen, es
          gut genug zu machen.
        </strong>
      </p>
      <p>
        Dieses Vertrauen hilft mir, mich in konkreten Situationen nicht zu
        herkömmlichen, behavioristischen Methoden verleiten zu lassen, weil
        diese kurzfristig mehr «Erfolg» versprechen. Ja, vielleicht würde meine
        Tochter manchmal schneller kooperieren, wenn ich ihr eine Belohnung
        versprechen oder mit einer Konsequenz drohen würde. Ja, vielleicht würde
        sie manchmal schneller wieder zufriedener erscheinen, wenn ich ihren
        Frust einfach ignorieren oder sie davon ablenken würde, anstatt sie
        geduldig darin zu begleiten. Dann hätte ich vielleicht kurzfristig das
        «richtige» Ergebnis, sprich das gehorsame und scheinbar glückliche Kind.
        Aber das auf Kosten der inneren Stärke zu erreichen, ist es mir nicht
        wert.
      </p>
      <p>
        Nora Imlau bringt es in ihrem wunderbaren Buch
        <em>«Mein Familienkompass. Was brauche ich und was brauchst du?»</em>
        wie folgt auf den Punkt:
      </p>
      <p>
        <em>
          «Wir alle sind mehr als das Ergebnis unserer Erziehung – das gilt auch
          und gerade für unsere Kinder. Deshalb gibt es zwar jede Menge Studien,
          die durchaus belegen, dass sichere Bindungserfahrungen und eine
          gewaltfreie Kindheit voller Wärme, Wertschätzung und Respekt
          statistisch gesehen ein ziemlich genialer Ausgangspunkt für ein
          gesundes, glückliches Leben darstellen. All diese
          Ausgangsvoraussetzungen sind jedoch kein Garant dafür, dass unser Kind
          deshalb irgendwann das Leben haben wird, das wir ihm wünschen. Diese
          Garantie gibt es nie. Auch wenn Erziehungsratgeber gern damit werben,
          das Rezept zu kennen, wie aus den Kindern von heute die besten
          Erwachsenen von morgen werden – letztlich haben wir es als Eltern
          nicht in der Hand, unsere Kinder zu guten Menschen zu machen.»
        </em>
      </p>
