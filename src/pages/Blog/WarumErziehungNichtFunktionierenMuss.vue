      <br /><br />
      <p>
        <strong>
          Das Erbe der behavioristischen Erziehung
        </strong>
      </p>
      <p>
        Die Idee, dass wir mit den richtigen Erziehungsmethoden die richtigen
        Ergebnisse erzielen können, sitzt tief im kollektiven Gedächtnis unserer
        Gesellschaft. Herkömmliche (sogenannte behavioristische) Methoden
        versprechen als Ergebnis vor allem das richtige Verhalten. Wir brauchen
        bloss die richtigen negativen oder positiven Anreize zu setzen und schon
        haben wir ein kooperative, rücksichtsvolles, hilfsbereites, dankbares
        (…) Kind.
      </p>
      <p>
        Offensichtlich gewaltvolle Anreize wie körperliche Züchtigung oder harte
        Strafen sind heute zwar weitgehend verpönt, aber «sanfte» Alternativen
        dazu findet man immer noch in vielen Ratgebern. Der «Schäm-di-Egge»
        wurde durch das Timeout oder den stillen Stuhl ersetzt, das Schimpfen
        durch das gezielte Ignorieren, die Strafe durch die logische Konsequenz
        oder gar durch eine Belohnungstafel. Die Grundidee und das Versprechen,
        mit den richtigen Reizen das richtige Verhalten zu erzielen, sind jedoch
        gleich geblieben. Gedanken dazu, warum solche verhaltenssteuernden
        Methoden unseren Kindern nicht gut tun (egal wie sanft sie scheinen),
        findest du im Artikel: «Ignorieren, Schimpfen, Strafen, Erpressen… Warum
        herkömmliche Erziehungsmethoden unseren Kindern schaden».
      </p>
