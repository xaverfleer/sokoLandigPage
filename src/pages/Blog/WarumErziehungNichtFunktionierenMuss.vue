      <p>
        Eine Mutter schrieb mir als Reaktion auf diesen Beitrag:
      </p>
      <p>
        <em
          >«Diese Strategien hören sich ja ganz nett an. Aber bei uns
          funktioniert keine. Mein Sohn akzeptiert mein Nein einfach nicht. Auch
          wenn ich ein Ja unter Bedingungen gebe oder ein Ja in der Fantasie,
          tut er trotzdem genau das, was ich ihm verboten habe (…). Und wenn ich
          dann auf meinem Nein beharre, bekommt er einen Gefühlsausbruch, weint,
          schreit, tobt, schlägt… Und dann seinen Frust zu bejahen verstärkt
          seinen Gefühlsausbruch nur noch mehr (…).»</em
        >
      </p>
      <p>
        Was für ein Frust. Diese Mutter hat sich viel versprochen von den
        Strategien und wurde bitter enttäuscht. Was genau hat sie sich
        (vermutlich) versprochen?
      </p>
      <ol>
        <li>
          Dass die Strategien einen unmittelbaren Einfluss auf das Verhalten
          ihres Sohnes haben, so dass er ein Nein ohne viel Widerstand
          akzeptieren (sprich gehorchen) kann.
        </li>
        <li>
          Dass die Strategien helfen, schwierige Gefühle fernzuhalten oder
          zumindest schneller zu überwinden, dass sie also unmittelbar zu mehr
          Zufriedenheit bei ihrem Sohn führen.
        </li>
      </ol>
      <p>
        Sie hat sich also überspitzt gesagt versprochen, dass diese Strategien
        ihr das gehorsame, glückliche Kind hervorbringen.
      </p>
      <br />
      <h2>Subjektorientierte Kommunikation kann "funktionieren"...</h2>
      <p>
        Ich fühle mich einerseits missverstanden und andererseits ertappt.
        Missverstanden, weil keine dieser Strategien das Ziel hat, dass ein Kind
        ein Nein plötzlich einfach so akzeptieren kann oder zumindest seinen
        Frust darüber möglichst schnell überwindet.
      </p>
      <p>
        Und ertappt, weil ich im besagten Beitrag meine Best-Practice-Beispiele
        wiedergebe, die tatsächlich hoffen lassen, dass diese Strategien in
        dieser Weise «funktionieren». JA, es war für mich beispielsweise eine
        ganz wunderbare Erfahrung, dem Wunsch meiner Tochter, abends nochmals
        aufzustehen, in der Fantasie nachzugehen und zu erleben, wie wir so
        einander nahe bleiben, anstatt in einen
        «Nein»-«Doch»-«Nein»-«Doch»-Kampf zu geraten.
      </p>
      <br />
      <h2>...muss aber nicht!</h2>
      <p>
        Aber NEIN, meine Tochter reagiert nicht jedes Mal so entspannt, wenn ich
        ihr anbiete, einen Wunsch in der Fantasie zu erfüllen. Kürzlich zum
        Beispiel wollte sie ins Hallenbad gehen. Auf der Suche nach ihrem
        Badetuch hat sie den ganzen Kleiderschrank leergeräumt. Ich habe ihr
        erklärt, dass wir nicht ins Hallenbad gehen können, weil die Bäder zu
        sind. Ich habe sie gebeten, die Kleider nicht aus dem Schrank zu räumen.
        Dann habe ich ihr vorgeschlagen, dass unser Bett in der Fantasie das
        Hallenbad ist und wir «Schwimmen-Gehen» spielen, ein Spiel, dass ihr
        schon oft Spass bereitet hat. Das Resultat: Zuerst hat sie weiterhin den
        Schrank ausgeräumt und als ich dazu weiterhin Nein sagte, hat sie
        angefangen zu weinen und zu toben… Und das eine gefühlte Ewigkeit,
        «obwohl» ich doch so viel Verständnis für ihren Frust gezeigt habe.
      </p>
      <p>
        In diesem Beispiel hat die Strategie «Ja in der Fantasie» meiner Tochter
        nicht geholfen, mein Nein besser zu akzeptieren. Und auch die Strategie
        «Ja zum Frust» hatte nicht zur Folge, dass sie sich schnell beruhigt hat
        und schnell wieder zufrieden war. Zeigt dieses Beispiel also, dass die
        Strategien nichts taugen, dass sie nicht «funktionieren»?
      </p>
      <p>
        Die Antwort auf diese Frage lautet:
      </p>
      <p>
        <strong
          >Ja klar zeigt dieses Beispiel, dass die Strategien nicht dazu taugen,
          ein braves und zufriedenes Kind zu haben. Denn eine solche
          unmittelbare Wirkung auf das Verhalten oder auf die Befindlichkeit
          unseres Kindes ist gar nicht das Ziel dieser Strategien und damit auch
          nicht ein sinnvolles Kriterium dafür, ob sie etwas taugen oder nicht.
          Es ist nicht sinnvoll, den Erfolg der subjektorientierten
          Kommunikation daran zu messen, wie sich ein Kind aktuell gerade
          verhält oder fühlt.</strong
        >
      </p>
      <p>
        Das Kernziel der subjektorientierten Kommunikation ist es, ein Kind
        innerlich zu stärken. Mehr zu diesem Thema findest du im frei
        zugänglichen ersten Kursblock im Video 1.2.
      </p>
