        Beziehung verliert. Sie trägt nach wie vor die Hautpverantwortung für
        das Wohlergehen aller Familienmitglieder und sie trifft auch die nötigen
        Entscheide dazu. Es ist klar, dass solche Entscheide manchmal gegen den
        Willen des Babys getroffen werden müssen, dann zum Beispiel, wenn das
        Baby durch eine Handlung die persönliche Grenze seiner Bezugsperson
        verletzt. Doch selbst dann ermöglicht eine gleichwürdige Sprache, die
        Wünsche und Interessen des Babys als berechtigt (wenn auch nicht
        erfüllbar) anzuerkennen und dem Baby zuzugestehen, Gefühle wie Frust,
        Trauer oder Wut zu empfinden.
      </p>
