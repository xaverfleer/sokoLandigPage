      <h2>Gleichwürdige Sprache</h2>
      <p>
        Eine gleichwürdige Sprache bringt zum Ausdruck, dass wir ein Kind in
        seiner individuellen Persönlichkeit mit seiner ganz eigenen inneren
        Realität wahr- und ernstnehmen. Eine gleichwürdige Sprache vermittelte
        dem Baby, dass seine Gefühle, Bedürfnisse und Wünsche gleichermassen
        wichtig und berechtigt sind, wie die Gefühle, Bedürfnisse und Wünsche
        seiner Bezugsperson. Der Alters- und Erfahrungsunterschied zwischen
        Erwachsenen und Babys darf nicht dazu führen, einem Baby weniger Würde
        beizumessen als einer erwachsenen Person.
      </p>
      <p>
        Einem Kind die gleiche Würde wie einer erwachsenen Person zuzugestehen,
