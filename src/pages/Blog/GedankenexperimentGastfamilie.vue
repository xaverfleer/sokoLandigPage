      <h2>Dressur schadet Kindern</h2>
      <p>
        Die herkömmlichen Erziehungsmethoden, die in deiner Gastfamilie
        angewendet werden, zielen primär darauf ab, dein Verhalten zu
        kontrollieren und zu steuern. Das Prinzip, das aus der Dressur von
        Tieren stammt, lautet: Reagiere auf schlechtes Verhalten mit negativen
        Reizen und/oder reagiere auf gutes Verhalten mit positiven Reizen.
      </p>
      <p>
        Die Verhaltensforschung zeigt, dass eine solche Erziehung tatsächlich
