        möglich, ein Kind ein Stück weit zu dressieren. Aus Angst vor negativen
        Reizen oder vor dem Entzug von in Aussicht gestellten positiven Reizen
        passen sich Kinder an. Ein Problem ist natürlich, dass die Reize mit
        steigendem Alter gesteigert werden müssen, um noch eine Wirkung zu
        entfalten. Da die Reize jedoch nicht endlos gesteigert werden können,
        entfällt dieses Erziehungsinstrument irgendwann. Erziehung in Form von
        Dressur mit positiven und negativen Reizen funktioniert also – wenn
        überhaupt – nur kurzfristig.
      </p>
      <p>
        Doch dieses Problem ist im Prinzip unwesentlich. Denn viel wesentlicher
        als die Frage, ob Dressur funktioniert oder nicht, ist die Frage, wie es
        Kindern dabei geht! Und auf diese Frage gibt es eine eindeutige Antwort:
        Diese herkömmlichen Erziehungsmethoden tun Kindern nicht gut. Sie
        schaden ihrer gesunden psychischen, kognitiven und sozialen Entwicklung
        massiv! Denn sie nehmen einem Kind das wichtigste, was es für eine
        gesunde Entwicklung braucht: Nämlich die Gewissheit, bedingungslos
        geliebt zu werden. Jedes Mal, wenn ein Kind erlebt, dass es ignoriert,
