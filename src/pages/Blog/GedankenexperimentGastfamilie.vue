        Entzug einer in Aussicht gestellten Belohnung), zweifelt es daran, als
        Mensch liebeswürdig und wertvoll zu sein. Denn es erlebt, dass ihm
        Liebe, Zuneigung und Wertschätzung entzogen bzw. vorenthalten oder nur
        unter bestimmten Bedingungen geschenkt werden, nämlich dann, wenn es
        sich angepasst verhält.
      </p>
