        sie mit ihrem Verhalten Grenzen überschreiten. Sie haben auch das Recht,
        sinnvolle und akzeptable Ausdrucksmöglichkeiten zu lernen (und das
        gelingt am besten, indem solche Ausdrucksmöglichkeiten im Alltag
        vorgelebt werden). Aber vor allem haben sie das Recht, nicht auf ihr
        Verhalten reduziert zu werden, sondern in ihren Gefühlen und
        Bedürfnissen, die sie mit ihrem Verhalten äussern, wahr- und
        ernstgenommen zu werden.
      </p>
