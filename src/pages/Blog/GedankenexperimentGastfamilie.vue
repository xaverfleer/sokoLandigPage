        liebeswürdig und unfähig ist. Ein Kind mit diesem Mindset ist anfällig
        für alle möglichen psychischen Erkrankungen und für Gruppendruck. Ebenso
        leiden seine Empathiefähigkeit und damit auch seine Sozialkompetenz.
        Denn Empathie – also die Fähigkeit die Perspektive anderer Menschen
        einzunehmen und mitzuberücksichtigen – entwickeln Kinder vor allem
        darüber, dass sie selbst empathisch behandelt werden. Erziehungsmethoden
        wie Ignorieren, Schimpfen, Strafen oder Erpressen sind aber alles andere
        als empathisch.
      </p>
      <br /><br />
      <h2>Jedes Verhalten ist Kommunikation!</h2>
      <p>
        Wenn uns die gesunde psychische, kognitive und soziale Entwicklung
        unserer Kinder am Herzen liegt, dann müssen wir von Erziehungsmethoden
        wegkommen, die einseitig auf die Kontrolle des Verhaltens abzielen.
        Stattdessen müssen wir anerkennen, dass jedes Verhalten – auch
        unangemessenes Verhalten – Kommunikation ist. Mit jedem Verhalten teilen
        Kinder etwas darüber mit, wie es ihnen geht und was sie brauchen. Es ist
        zentral, dass wir lernen, diese Botschaften zu entschlüsseln und darauf
        einzugehen.
      </p>
