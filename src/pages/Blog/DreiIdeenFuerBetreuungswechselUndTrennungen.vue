      <h2>Idee Nummer 2: Eine Aktivitätenliste schreiben</h2>
      <p>
        Vor kurzem haben wir entschieden, dass unsere Tochter eine Nacht und
        einen Tag bei ihrer Oma «in die Ferien» geht. Sie kennt die Oma gut und
        hat eine gute Beziehung. Als wir unseren etwas längeren Abschied
        ankündeten, gab es dennoch einige Tränen. Völlig verständlich und in
        Ordnung. Wir haben diese Tränen mit verständnisvollen Worten («Du bist
        gerade traurig, dass wir gleich gehen, nicht wahr?») und vor allem mit
        ganz viel Nähe und Zuneigung begleitet. Nachdem die Tränen etwas
        versiegt waren, haben wir unserer Tochter vorgeschlagen, mit Oma eine
        Liste zu erstellen, mit allem, was sie in diesen kurzen Ferien gemeinsam
        tun könnten. Unsere Tochter hat sich sofort darauf eingelassen und ihrer
        Oma einige Ideen zum Aufschreiben diktiert (z.B. im Sandkasten spielen).
        Ihre Oma hat weitere Ideen vorgeschlagen, so dass eine ziemlich lange
        Liste entstand. Natürlich kann unsere Tochter noch nicht lesen, deswegen
        hat die Oma die Liste mit kleinen Zeichnungen ergänzt/visualisiert.
        Danach haben die beiden vereinbart, mit welchem Listenpunkt sie beginnen
        wollen, sobald wir Eltern weg sein würden. Beim Abschied gab es nochmals
        ein kleines Tränchen, doch noch während wir ins Auto stiegen, rannte
        meine Tochter zur Liste und wollte unbedingt mit all den tollen
        Aktivitäten beginnen, die darauf standen.
      </p>
      <g-image
        alt="Aktivitätenliste"
        class="img"
        src="~/assets/imgs/liste.jpg"
      />
      <p>
        Wenn ein Betreuungswechsel bzw. ein Abschied bevorsteht, sind die
        schwierigen Gefühle in Bezug auf den Übergang und die Trennung für ein
        Kind manchmal überwältigend. Es wird davon so stark vereinnahmt, dass es
        nichts anderes mehr denken oder fühlen kann. Hier kann es helfen, den
        Blick des Kindes etwas zu weiten und auf die schönen Aktivitäten, die
        ihm nach dem Übergang bevorstehen, zu lenken. Entweder bevor das Kind
        von den schwierigen Gefühlen überrumpelt wird oder nachdem es sich
        wieder etwas beruhigen konnte.
      </p>
      <p>
        Dazu können wir das Kind fragen, was es denn nach dem Betreuungswechsel
        gerne mit der neuen Betreuungsperson unternehmen möchte oder worauf es
        sich denn freue. Dann können wir das, was uns möglich erscheint, auf
        eine Liste schreiben (z.B. Seifenblasen machen, ein Buch anschauen, eine
        Banane essen, auf den Spielplatz gehen…). Natürlich können wir auch
        eigene Vorschläge einbringen. Die Liste können wir entweder vor unserem
        Abschied mit dem Kind erstellen und sie dann der neuen Betreuungsperson
        übergeben oder wir können unser Kind und die neue Betreuungsperson dazu
        ermuntern, gemeinsam eine Liste zu erstellen, während wir noch anwesend
        sind.
      </p>
      <p>
        Es spielt keine Rolle, ob das Kind schon lesen kann oder noch nicht.
        Allenfalls können wir die Liste durch kleine Zeichnungen ergänzen, um
        sie für das Kind zu visualisieren. rsagensängste zu entwickeln.
      </p>
      <p>
        Das gemeinsame Erstellen einer solchen Liste hat verschiedene Vorteile:
      </p>
      <ul>
        <li>
          Das Kind kann Vorfreude auf die neue Betreuungssituation entwickeln,
          indem es seinen Fokus auf schöne Aktivitäten lenkt, die nach dem
          Übergang möglich sind.
        </li>
        <li>
          Das Kind erlebt, dass seine Ideen ernst genommen werden. Es spielt
          keine Rolle, ob alle Listenpunkte abgehakt werden können. Allein das
          Aufschreiben davon, was alles möglich wäre, kann einem Kind helfen,
          sich gesehen zu fühlen.
        </li>
        <li>
          Die Liste schafft eine Orientierung. Sie kann gut sichtbar für das
          Kind und für die Betreuungsperson aufgehängt und immer wieder
          konsultiert werden.
        </li>
      </ul>
      <p>
        Manchmal erstellen wir auch vor einer Trennung eine Liste für die Zeit
        nach dem Widersehen. Wir fragen z.B. unsere Tochter am Morgen, was wir
        am Nachmittag nach der Kita noch gemeinsam unternehmen könnten und ob
        sie eine Liste erstellen wolle. Wenn wir dann unsere Tochter von der
        Kita abholen, bringen wir die Liste mit und schauen, worauf wir noch
        Lust haben.
      </p>
      <br />
      <h2>Idee Nummer drei: Eine visualisierte Wochenübersicht erstellen</h2>
      <p>
        Eine Wochenübersicht der verschiedenen Betreuungssituationen kann einem
        Kind Orientierung und Struktur geben und ihm dadurch Übergänge
        erleichtern. Dazu kann man z.B. ein Whiteboard, eine Wandtafel oder eine
        Pinwand in sieben Spalten (für jeden Wochentag) und in drei Zeilen
        (Vormittag, Nachmittag, Abend) einteilen. In den einzelnen Feldern
        visualisiert man nun die Betreuungssituation. Dazu kann man z.B.
        Fotomagnete mit Fotos der verschiedenen Betreuungspersonen herstellen
        lassen. Man kann Fotos ausdrucken oder Kärtchen zeichnen.
      </p>
      <p>
        Den aktuellen Wochentag kann man mit einer Klammer/mit Farbe/mit einem
        Magnet oder ähnlichem markieren.
      </p>
      <g-image
        alt="Wochenplan"
        class="img"
        src="~/assets/imgs/wochenplan.jpg"
      />
      <p>
        Zusätzlich kann man Magnete/Kärtchen mit wiederkehrenden Aktivitäten
        (z.B. Babyschwimmen, Krabbelgruppe, Einkaufen, Spielplatz usw.)
        erstellen und so auf auch gleich visualisieren, was auf dem
        Tagesprogramm steht.
      </p>
