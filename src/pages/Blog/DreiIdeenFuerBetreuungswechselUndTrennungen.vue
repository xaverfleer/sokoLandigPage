      <h1 class="blog__title">
        Drei Ideen, um einem Kind Betreuungswechsel und Trennungen zu
        erleichtern
      </h1>
      <p>
        Der Übergang von einer Betreuungssituation zu einer anderen (z.B. von
        Eltern- zu Kitazeit oder von Mama- zu Papazeit) fällt vielen Kindern
        schwer. Manche Kinder weinen und klammern sich beim Übergang, der ja
        auch immer ein Abschied ist, an ihre (primäre) Bezugsperson.
      </p>
      <p>
        Als Mutter weiss ich, wie schwer solche tränenreichen Übergänge nicht
        nur für das Kind, sondern auch für uns Eltern sind. Ein weinendes Kind
        zu verlassen kann ganz schön schmerzen, auch wenn wir wissen, dass es
        gut aufgehoben ist.
      </p>
      <p>
        In diesem Artikel zeige ich drei Ideen auf, wie wir Eltern unseren
        Kindern einen Betreuungsübergang erleichtern können. Es ist mir wichtig
        vorab folgendes klarzustellen: Unsere Kinder dürfen weinen, wenn sie
        Trennungsschmerz empfinden und wenn ihnen Übergänge zu schaffen machen!
      </p>
      <ol>
        <li>
          <strong
            >Abschiedstränen bedeuten nicht, dass wir als Eltern etwas falsch
            gemacht haben!</strong
          >
          <br />Im Gegenteil: Aus Sicht der Bindungstheorie ist es insbesondere
          gerade bei kleinen Kindern ganz normal und ein Zeichen einer sicheren
          Bindung, wenn sie bei Trennungssituationen weinen. Es ist kein
          schlechtes Zeichen, wenn kleine Kinder bei ihren Bindungspersonen sein
          wollen und sich auch getrauen, dies zu zeigen. Das heisst nun wiederum
          nicht, dass unsere Bindung zum Kind gestört ist, wenn das Kind nicht
          weint. Kinder bringen nun mal unterschiedliche Temperamente und
          Persönlichkeitseigenschaften mit und gehen mit schwierigen Situationen
          unterschiedlich um.
        </li>
        <br />
        <li>
          <strong
            >Abschiedstränen bedeuten auch nicht, dass etwas mit unserem Kind
            nicht stimmt,</strong
          >
          <br />dass es z.B. noch zu unselbständig oder zu schüchtern ist.
          Leider werden viele Eltern oft mit der Idee konfrontiert, dass es
          lobenswert ist, wenn ein Kind Trennungssituationen früh einfach
          wegsteckt. Dabei wird aber oft übersehen, dass manche Kinder auch
          einfach deswegen nicht weinen, weil sie resigniert haben («es nimmt
          mich ja sowieso niemand ernst») oder weil sie Angst vor einer
          negativen Reaktion auf ihr Weinen haben («Wenn ich weine, verliere ich
          die Liebe/Geduld/Zuneigung meiner Bezugsperson»). Solche Kinder kommen
          dann zwar vordergründig gut mit Trennungssituationen und
          Betreuungswechseln klar. Misst man jedoch die Werte des Stresshormons
          Cortisol, kann man feststellen, dass es ihnen innerlich nicht gut
          geht. Nur haben sie gelernt, es nicht zu zeigen.
        </li>
      </ol>
      <p>
        Kinder haben also das Recht, bei einem Abschied bzw. bei einem
        Betreuungswechsel zu weinen, daran ist nichts verkehrt. Dennoch können
        wir versuchen, die Übergangssituationen für das Kind etwas einfacher zu
        gestalten. Keine der folgenden drei Ideen zielt jedoch darauf ab, das
        Weinen zu vermeiden oder abzuschalten. Der Fokus der Ideen liegt darauf,
        die Übergänge, Abschiede und Betreuungswechsel vorhersehbar zu machen.
        <br /><br />
      </p>

      <h2>Idee Nummer 1: Die Zeit bis zum Wechsel visualisieren</h2>
      <p>
        Betreuungswechsel fallen Kindern besonders schwer, wenn sie für sie
        völlig unvorhersehbar und plötzlich geschehen. Es ist grundsätzlich
        hilfreich, einen bevorstehenden Wechsel anzukünden. Spezielle Uhren
        (oder andere Hilfsmittel) können uns dabei dienen, die verbleibende Zeit
        bis zu einem Betreuungswechsel zu visualisieren.
      </p>
      <p>
        Die Visualisierung der Zeit kann einem Kind helfen, sich auf einen
        Betreuungswechsel einzustellen. Das Kind hat so eine Orientierung und
        wird nicht plötzlich von einem Wechsel überrascht. Auch wenn das Kind
        eine Bezugsperson vermisst und diese in absehbarer Zeit nach Hause
        kommt, kann ein TimeTimer helfen, das Warten auf die Bezugsperson
        einfacher zu machen.
      </p>
      <p>
        Wir benutzen zuhause oft einen sogenannten TimeTimer. Auf dem TimeTimer
        können wir (wie bei einem normalen Timer) einstellen, wie viel Zeit bis
        zu einem bestimmten Event noch vergeht. Die verbleibende Dauer wird
        dabei farblich visualisiert: Entsprechend der ausgewählten Zeit wird
        eine grössere oder kleinere Fläche des Kreises farbig markiert. Mit
        fortschreitender Zeit wird diese Fläche kleiner, bis sie schliesslich
        verschwindet, wenn die eingestellte Zeit vorbei ist.
      </p>
      <g-image alt="TimeTimer" class="img" src="~/assets/imgs/timetimer2.jpg" />
      <p>
        Wir haben zuahsue das Modell
        <a href="https://shop.muelicom.ch/Time-Timer-MOD" target="_blank">
          TimeTimer MOD >></a
        >. Ein sehr nützliches Feature davon ist, dass man durch einen Schieber
        einstellen kann, ob die Uhr am Ende des Zeitfensters einen Signalton von
        sich gibt oder nicht (dies ist keine bezahlte Werbung, nur eine
        persönliche Empfehlung). TimeTimer sind auch als App verfügbar.
      </p>
      <p>
        TimeTimer können nicht nur bei Betreuungswechseln hilfreich sein,
        sondern insgesamt bei Übergängen (z.B. wenn wir in zehn Minuten von
        Zuhause loswollen) oder Wartzeiten (z.B. wenn wir fünfzehn Minuten Zeit
        zum Telefonieren brauchen).
      </p>
      <p>
        Natürlich können wir die Zeit für unser Kind auch auf andere Weisen
        visualisieren, z.B. mit klassischen Sanduhren. Eine Idee zum
        Selberbasteln ist die folgende: Wir können die beiden Zeiger einer Uhr
        mit zwei unterschiedlichen Farben einfärben. Zusätzlich färben wir zwei
        Klammern mit den gleichen zwei Farben ein. Um einen bestimmten Zeitpunkt
        zu visualisieren können wir nun die Klammern an den Stellen an der Uhr
        anbringen, auf die die Zeiger zum bestimmten Zeitpunkt zeigen werden.
      </p>
